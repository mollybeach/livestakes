export const getLivestreams = async (): Promise<LivestreamDataType[]> => {
  await new Promise((resolve) => setTimeout(resolve, 100));
  return mockLivestreams;
};

export const getLivestreamById = async (id: number): Promise<LivestreamDataType | null> => {
  await new Promise((resolve) => setTimeout(resolve, 100));
  return mockLivestreams.find(livestream => livestream.id === id) || null;
};

export const getLivestreamsByCategory = async (category: string): Promise<LivestreamDataType[]> => {
  await new Promise((resolve) => setTimeout(resolve, 100));
  return mockLivestreams.filter(livestream => livestream.category === category);
};

export const getActiveLivestreams = async (): Promise<LivestreamDataType[]> => {
  await new Promise((resolve) => setTimeout(resolve, 100));
  return mockLivestreams.filter(livestream => livestream.status === 'active');
};
